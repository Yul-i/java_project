package 클래스생성;

public class 입력값연습 {
	public void add(int x, int y) {
		System.out.println(x + y);
	}

	public void minus(int x) {
		System.out.println(x - 100);
	}

	public void mul(int x, String y) {
		int result = x * 10;
		System.out.println(y + "가 " + result + "원을 냈습니다.");
	}
}
